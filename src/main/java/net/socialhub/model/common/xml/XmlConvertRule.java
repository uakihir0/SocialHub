package net.socialhub.model.common.xml;

public class XmlConvertRule {

    /** p tag */
    private String p = "\n";

    /** br tag */
    private String br = "\n";

    //region // Getter&Setter
    public String getP() {
        return p;
    }

    public void setP(String p) {
        this.p = p;
    }

    public String getBr() {
        return br;
    }

    public void setBr(String br) {
        this.br = br;
    }
    //endregion
}
